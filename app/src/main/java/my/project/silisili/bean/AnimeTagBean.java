package my.project.silisili.bean;

public class AnimeTagBean {
    private String name;
    private String year;

    public AnimeTagBean(String name, String year) {
        this.name = name;
        this.year = year;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getYear() {
        return year;
    }

    public void setYear(String year) {
        this.year = year;
    }
}
